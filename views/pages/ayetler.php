			<p>268: Şeytan sizi fakirlikle korkutup &ccedil;irkin şeylere teşvik eder. Allah da l&uuml;tfundan ve bağışlamasından birtakım vaatlerde bulunuyor. Allah&#39;ın l&uuml;tfu geniştir. O her şeyi bilendir.</p>

			<p>275: Riba (Faiz) yiyen kimseler, şeytan &ccedil;arpan kimse nasıl kalkarsa ancak &ouml;yle kalkarlar. Bu ceza onlara, &quot; alışveriş de faiz gibidir &quot; demeleri y&uuml;z&uuml;ndendir. Oysa Allah, alışverişi helal, faizi de haram kılmıştır...</p>

			<p>AL-İ İMRAN SURESİ</p>

			<p>36: Onu doğurunca ş&ouml;yle dedi: &quot;Rabbim, onu kız doğurdum; erkek, kız gibi değildir. Ona Meryem adını verdim. Onu ve soyunu kovulmuş şeytanın şerrinden sana ısmarlıyorum</p>

