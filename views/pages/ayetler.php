			<p>103: Şayet onlar iman edipte korunmuş olsalardı, elbette Allah tarafından verilecek m&uuml;kafat &ccedil;ok daha hayırlı olacaktı. Keşke bunu bilselerdi.</p>

			<p>168: Ey insanlar! B&uuml;t&uuml;n yery&uuml;z&uuml;ndeki nimetlerimden helal olmak, temiz olmak şartıyla yiyin. Fakat şeytanın adımlarına uymayın. &Ccedil;&uuml;nk&uuml; o size belli bir d&uuml;şmandır.</p>

