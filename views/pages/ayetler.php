			<p>NİSA SURESİ</p>

			<p>38: Bunlar, Allah&#39;a ve ahiret g&uuml;n&uuml;ne iman etmedikleri halde mallarını, insanlara g&ouml;steriş yapmak i&ccedil;in harcarlar. Şeytan kimin arkadaşı olursa o ne k&ouml;t&uuml; arkadaştır!</p>

			<p>51: Şu kendilerine kitaptan bir nasip verilmiş olanları g&ouml;rm&uuml;yor musun! Onlar puta ve şeytana inanıyorlar. Bir de kalkıp kafirler hakkında &quot;Onlar, m&uuml;sl&uuml;manlardan daha doğru yoldadır&quot; diyorlar!</p>

			<p>60: Şunları g&ouml;rm&uuml;yor musun? Kendilerinin sana indirilene ve senden &ouml;nce indirilene inandıklarını ileri s&uuml;r&uuml;yorlar da tağuta inanmamaları kendilerine emrolunduğun halde, tağut &ouml;n&uuml;nde muhakemeleşmek istiyorlar. Şeytan da onları bir daha d&ouml;nemeyecekleri kadar iyice sapıklığa d&uuml;ş&uuml;rmek istiyor.</p>

			<p>76: İman edenler, Allah yolunda savaşırlar. İnkar edenler de tağut yolunda savaşırlar. O halde siz şeytanın taraftarlarına karşı savaşın. &Ccedil;&uuml;nk&uuml; şeytanın hilesi zayıftır.</p>

