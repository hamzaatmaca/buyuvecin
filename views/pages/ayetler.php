			<p>116: Ş&uuml;phesiz Allah kendisine ortak koşulmasını bağışlamaz. Bunun dışında dilediğini bağışlar. Allah&#39;a ortak koşan, muhakkak ki, derin bir sapıklığa d&uuml;şm&uuml;şt&uuml;r.</p>

			<p>117: Onlar, Allah&#39;ı bırakırlar da, yalnız dişilere taparlar. B&ouml;ylece ancak inat&ccedil;ı şeytana tapmış olurlar.</p>

			<p>118: Allah o şeytana lanet etti. Ve o da: &quot;Elbette senin kullarından belirli bir pay alacağım, onları mutlaka saptıracağım, onları boş kuruntularla oyalayacağım.&quot;</p>

