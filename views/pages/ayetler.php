			<p>34: Ve meleklere: &quot; Adem&#39;e secde edin &quot; dedi. Melekler hemen secde ettiler. Yalnız İblis kibrinin esiri oldu ve secde etmedi. İnkarcılardan oldu.</p>

			<p>35: Allah: &quot; Ey Adem, sen ve eşin cennette oturun, ikinizde ondan dilediğiniz yerden bol bol yiyin, fakat şu ağaca yaklaşmayın, yoksa zalimlerden olursunuz&quot;</p>

			<p>36: Fakat Şeytan, ayaklarını kaydırdı, i&ccedil;inde bulundukları cennet yurdundan &ccedil;ıkarılmalarına sebep oldu. Biz de: &quot; Birbirinize d&uuml;şman olarak inin, orada belirli bir vakte kadar sizin i&ccedil;in bir karar yeri ve bir nasip vardır&quot; dedik.</p>

			<p>37: Adem Rabbinden &ouml;ğrendiği kelimelerle tevbe etti. Allah&#39;ta tevbesini kabul etti. Muhakkak O, tevbeyi &ccedil;ok kabul eden, &ccedil;ok esirgeyendir.</p>

			<p>38: Onlara dedik ki: &quot;Hepiniz oradan inin. Size benim tarafımdan bir hidayet rehberi geldiğinde, kim onun izinden giderse, onlar i&ccedil;in bir korku yoktur, onlar mahzun da olmayacaklardır.</p>

			<p>39: İnkar edip ayetlerimizi yalanlayanlara gelince, onlar cehennem ehli olarak ebediyen orada kalacaklardır.</p>

