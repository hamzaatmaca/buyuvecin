			<p>120: Şeytan onlara vaad eder ve onları boş umutlar ile oyalar. Oysa şeytanın onlara vaadi, aldatmadan başka birşey değildir.</p>

			<p>121: Bunların varacakları yer cehennemdir. Ondan kurtulmak i&ccedil;in &ccedil;are bulamazlar.</p>

			<p>MAİDE SURESİ</p>

