	<h2 id="textHead" class="g-font-size-32--xs g-font-size-36--md g-text-center--xs g-margin-b-80--xs">Kur'an-ı Kerim'de Cinler-Şeytanlar İle İlgili Ayetler</h2>
	<div class="g-text-left--xs">
		<div>
			<p>Giriş b&ouml;l&uuml;m&uuml;nde bahsedildiği gibi gayb alemi ile ilgili bilgileri, hem o alemi hem de yaşadığımız alemi yaratan ve y&ouml;neten Allah tarafından g&ouml;nderilmiş ilahi kitaplardan ve g&ouml;revli peygamberlerden &ouml;ğrenebiliriz. Bunun dışındaki bilgiler ger&ccedil;ek dışı ve yanıltıcıdır. Museviliğin ve Hıristiyanlığın kutsal kitapları olan, Tevrat ve İncil&#39;in ger&ccedil;ek n&uuml;shaları ortada yoktur, kaybolmuştur. Mevcut olanlar ise insanlar tarafından sonrada yazılmış veya değiştirilmiştir. Bu y&uuml;zden bu kitaplardaki bilgilere itibar edilmez. İnsanlığın elindeki yegane ilahi kitap, tahrif olmayan ve Allah tarafından korunan Kur&#39;an-ı Kerimdir. M&uuml;racaat edilecek tek ve doğru kaynak bu kitaptır. Bu nedenle Kur&#39;anda yer alan, gayb alemiyle ilgili ayetleri okuyarak &ouml;zellikle cin, şeytan ve b&uuml;y&uuml; ile ilgili hakikate uygun bilgileri buradan &ouml;ğrenebiliriz. Aşağıda konu ile ilgili ayetler, surelere g&ouml;re tasnif edilerek &ccedil;ıkarılmıştır. Sitemizde b&uuml;y&uuml;-cin musallatı ile ilgili t&uuml;m a&ccedil;ıklamalar, bu ayetler kaynak alınarak yapılmaya &ccedil;alışılmıştır. Ayrıca, konuyla ilgili bilgiler, buradan da incelenerek &ouml;ğrenilebilir. Ayetlerin daha ayrıntılı a&ccedil;ıklamalarını &ouml;ğrenmek isteyenler Tefsir eserlerine başvurmalıdırlar.</p>

			<p>BAKARA SURESİ</p>

			<p>30: Bir zamanlar Rabbin meleklere: &quot;Ben yery&uuml;z&uuml;nde bir halife yaratacağım&quot; demişti. Melekler: &quot;Biz seni &ouml;verek tesbih ederken bozgunculuk yapacak ve kan d&ouml;kecek birisini mi yaratacaksın?&quot; dediler. Allah meleklere, &quot;Ben sizin bilmediklerinizi bilirim&quot; dedi.</p>

			<p>31: Allah, Adem&#39;e isimlerin hepsini &ouml;ğretti, sonra onları meleklere g&ouml;sterip: &quot;Haydi s&ouml;ylediklerinizde sadıksanız bana şunları isimleriyle haber verin&quot; dedi.</p>

			<p>32: Melekler dediler ki, ya Rabbi, sen y&uuml;cesin. Bizim, senin bize &ouml;ğrettiğinden başka bir bilgimiz yoktur. Ş&uuml;phesiz sen bilensin, hakimsin&quot;</p>

